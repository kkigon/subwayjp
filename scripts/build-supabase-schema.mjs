import { readFile, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const parts = [
  "core.sql",
  "versus-step1-rooms.sql",
  "versus-multiplayer-authority.sql",
  "versus-public-rooms-chat.sql",
  "versus-game-state.sql",
];
const contents = await Promise.all(parts.map(name => readFile(join(root, "supabase", name), "utf8")));
const header = `-- サブウェイ・ゲッサー日本版 / 韓国版と同じSupabaseに安全に共存\n-- 日本版のテーブル・RPCはすべてjp_接頭辞で分離されています。\n-- Supabase SQL Editorでこのファイル全体を実行してください。\n-- Generated by scripts/build-supabase-schema.mjs. Edit source fragments instead.\n\n`;
await writeFile(join(root, "supabase", "schema.sql"), header + contents.join("\n\n") + "\n", "utf8");
console.log(`Built supabase/schema.sql from ${parts.length} fragments.`);
